    public void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            Connection c = Koneksi.ConfigDB();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM paket";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[3];
                o[0] = r.getString("ID_Paket");
                o[1] = r.getString("Nama_Paket");
                o[2] = r.getString("Harga");

                model.addRow(o);
            }
            r.close();
            s.close();

        } catch (Exception e) {
            System.out.println("Terjadi Kesalahan");
        }
    }

    public void cariData() {
        DefaultTableModel tabel = new DefaultTableModel();

        tabel.addColumn("ID Paket");
        tabel.addColumn("Nama Paket");
        tabel.addColumn("Harga");

        try {
            Connection c = Koneksi.ConfigDB();
            String sql = "Select * from paket where ID_Paket like '%" + txCari.getText() + "%'"
                    + "or Nama_Paket like '%" + txCari.getText() + "%'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3)
                });
            }
            tbPaketHemat.setModel(tabel);
        } catch (Exception e) {
            System.out.println("Cari Data Error");
            e.printStackTrace();
        }
    }
