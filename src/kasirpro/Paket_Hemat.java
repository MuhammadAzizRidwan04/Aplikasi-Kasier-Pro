    private void nomorOtomatis() {
        try {
            Connection c = Koneksi.ConfigDB();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM paket ORDER BY ID_Paket DESC";
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                String NoFaktur = r.getString("ID_Paket").substring(2);
                String BR = "" + (Integer.parseInt(NoFaktur) + 1);
                String Nol = "";

                if (BR.length() == 1) {
                    Nol = "00";
                } else if (BR.length() == 2) {
                    Nol = "0";
                } else if (BR.length() == 3) {
                    Nol = "";
                }

                txIDPaket.setText("PK" + Nol + BR);
            } else {
                txIDPaket.setText("PK001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("NomorOtoamtis error");
        }
    }

